<h1>Network Monitoring Tool</h1>
<p1 style="font-family:courier; text-align:center; font-size:100%;">
Network Monitoring Tool enables one to log and monitor data flowing in and out of the network. The program runs as a local service. It helps administrator to check for congestion in the network and the root cause for it. It can be useful in troubleshooting packet loss and latency.
</p1><br>
<p1 style="margin-left: 3em; font-family:courier; text-align:center; font-size:100%;">
The tool has been designed for TCP (Transmission Control Protocol), UDP (User Datagram Protocol) and ARP (Address Resolution Protocol).It traces timestamp, source-MAC, destination-MAC, source IP, destination IP, source port, destination port and packet length. To keep track of all the communication and all the data transfer taking place, we need various network tools. These tools constantly monitor a network for slow or failing components and notifies the network administrator.
<br><br></p1>
<p1 style="margin-left: 0em; font-family:courier; text-align:center; font-size:100%;">
    Network Monitoring Tool lets us know how well the network is running during the course of ordinary operations. It is mainly for monitoring and for analyzing the network traffic between various terminals and systems and then getting a record of all those data into the data files and then to the database. After dumping of data it is then analyzed for any problem in the data transferred by the host machines connected across the network. If any host behaves abnormally we can easily retrieve the dumped data associated with that host in order to tackle the problem related to that host. If still needed yet another tool decrypting of data can be used to analyze the problem.
<br></p1>
<p1 style="margin-left: 4em; font-family:courier; text-align:center; font-size:100%;">
IPv4 Network Monitoring Tool has been designed on the LAMP architecture platform. 
LAMP is an open source Web development platform that uses Linux as the operating system, Apache as the Web server,  MYSQL as the relational database management system and PHP/Perl/Python as the object-oriented scripting language. 
